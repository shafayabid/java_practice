package org.example;

public class Human {
    private int age;
    private String name;

    //getter methods
    public int getAge(){
        return age;
    }

    public String getName(){
        return name;
    }

    //setter methods
    public void setAge(int age){
        this.age = age;
    }

    public void setName(String name){
        this.name = name;
    }
}
